const mongoose = require("mongoose");

const schema = mongoose.Schema({
    firstname: {
        type: String,
        required: true
    },
    lastname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    }
})

const firstschema = mongoose.model("Corporate Admin panel", schema);

module.exports = firstschema;
